package color

import (
	"fmt"

	"github.com/talwat/oh-my-go/internal/global"
)

const (
	Reset         = "\x1B[0m"
	Bold          = "\x1B[1m"
	Dim           = "\x1B[2m"
	Italic        = "\x1B[3m"
	URL           = "\x1B[4m"
	Blink         = "\x1B[5m"
	Blink2        = "\x1B[6m"
	Selected      = "\x1B[7m"
	Hidden        = "\x1B[8m"
	Strikethrough = "\x1B[9m"

	Black   = "\x1B[30m"
	Red     = "\x1B[31m"
	Green   = "\x1B[32m"
	Yellow  = "\x1B[33m"
	Blue    = "\x1B[34m"
	Magenta = "\x1B[35m"
	Cyan    = "\x1B[36m"
	White   = "\x1B[37m"

	BrightBlack   = "\x1B[30;1m"
	BrightRed     = "\x1B[31;1m"
	BrightGreen   = "\x1B[32;1m"
	BrightYellow  = "\x1B[33;1m"
	BrightBlue    = "\x1B[34;1m"
	BrightMagenta = "\x1B[35;1m"
	BrightCyan    = "\x1B[36;1m"
	BrightWhite   = "\x1B[37;1m"
)

func PromptColor(color string) string {
	closerStart := ""
	closerEnd := ""

	switch global.Shell {
	case "zsh":
		closerStart = "%%{"
		closerEnd = "%%}"
	case "bash":
		closerStart = "\001"
		closerEnd = "\002"
	}

	return fmt.Sprintf("%s%s%s", closerStart, color, closerEnd)
}
